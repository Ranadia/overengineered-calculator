package main

func plus(a float64, b float64) float64 {
	return a + b
}

func minus(a float64, b float64) float64 {
	return a - b
}

func multiply(a float64, b float64) float64 {
	return a * b
}

// should handle divide by zero calculations.
func divide(a float64, b float64) float64 {
	return a / b
}
